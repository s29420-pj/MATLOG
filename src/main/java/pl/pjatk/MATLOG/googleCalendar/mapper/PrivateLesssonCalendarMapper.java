package pl.pjatk.MATLOG.googleCalendar.mapper;

import pl.pjatk.MATLOG.domain.PrivateLesson;
import pl.pjatk.MATLOG.googleCalendar.dto.PrivateLessonCalendarDTO;

public class PrivateLesssonCalendarMapper {
    public PrivateLessonCalendarDTO toPrivateLessonCalendarDTO(PrivateLesson privateLesson) {
        return new PrivateLessonCalendarDTO(
                privateLesson.getId(),
                privateLesson.getSchoolSubjects(),
                privateLesson.getTutorId(),
                privateLesson.getStudentId(),
                privateLesson.getConnectionCode(),
                privateLesson.getStatus(),
                privateLesson.isAvailableOffline(),
                privateLesson.getStartTime(),
                privateLesson.getEndTime(),
                privateLesson.getPrice()
        );
    }
}
